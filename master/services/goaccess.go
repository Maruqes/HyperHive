package services

import (
	"bytes"
	"fmt"
	"os"
	"os/exec"
	"path/filepath"
	"sort"
	"strings"
)

const (
	goAccessConfigContent = `# Auto-generated by 512SvMan
time-format %H:%M:%S
date-format %d/%b/%Y
log-format [%d:%t %^] %^ %^ %s %^ %^ %^ %v "%U" [Client %h] [Length %b] [Gzip %^] [Sent-to %^] "%u" "%R"
`
	goAccessScriptContent = `#!/usr/bin/env bash
set -euo pipefail

BASE_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
LOG_DIR="${BASE_DIR}/logs"
CONF_FILE="${BASE_DIR}/goaccess.conf"

if ! command -v goaccess >/dev/null 2>&1; then
  echo "goaccess binary not found in PATH" >&2
  exit 1
fi

readarray -t files < <(find "$LOG_DIR" -maxdepth 1 -type f \( -name '*_access.log' -o -name 'fallback_access.log' \) | sort)
if [ "${#files[@]}" -eq 0 ]; then
  echo "No access logs found in $LOG_DIR" >&2
  exit 1
fi

exec goaccess "${files[@]}" --config-file="$CONF_FILE" "$@"
`
)

// EnsureGoAccessAssets creates the goaccess config, helper script, and log directory.
func EnsureGoAccessAssets() (logDir string, confPath string, err error) {
	workDir, err := os.Getwd()
	if err != nil {
		return "", "", fmt.Errorf("getwd: %w", err)
	}

	npmDataDir := filepath.Join(workDir, "npm-data")
	logDir = filepath.Join(npmDataDir, "logs")
	if err := os.MkdirAll(logDir, 0o755); err != nil {
		return "", "", fmt.Errorf("create log dir: %w", err)
	}

	confPath = filepath.Join(npmDataDir, "goaccess.conf")
	if err := os.WriteFile(confPath, []byte(goAccessConfigContent), 0o644); err != nil {
		return "", "", fmt.Errorf("write GoAccess config: %w", err)
	}

	scriptPath := filepath.Join(npmDataDir, "goaccess-run.sh")
	if err := os.WriteFile(scriptPath, []byte(goAccessScriptContent), 0o755); err != nil {
		return "", "", fmt.Errorf("write GoAccess helper script: %w", err)
	}
	if err := os.Chmod(scriptPath, 0o755); err != nil {
		return "", "", fmt.Errorf("chmod GoAccess helper script: %w", err)
	}

	return logDir, confPath, nil
}

// GenerateGoAccessReport runs goaccess against the NPM access logs and returns the HTML report.
func GenerateGoAccessReport() ([]byte, error) {
	goAccessPath, err := exec.LookPath("goaccess")
	if err != nil {
		return nil, fmt.Errorf("goaccess binary not found in PATH")
	}

	logDir, confPath, err := EnsureGoAccessAssets()
	if err != nil {
		return nil, err
	}

	entries, err := os.ReadDir(logDir)
	if err != nil {
		return nil, fmt.Errorf("read log dir: %w", err)
	}

	var logFiles []string
	for _, entry := range entries {
		if entry.IsDir() {
			continue
		}
		name := entry.Name()
		if strings.HasSuffix(name, "_access.log") || name == "fallback_access.log" {
			logFiles = append(logFiles, filepath.Join(logDir, name))
		}
	}

	if len(logFiles) == 0 {
		return nil, fmt.Errorf("no access log files found in %s", logDir)
	}

	sort.Strings(logFiles)

	args := []string{"--config-file=" + confPath, "--output=-"}
	for _, file := range logFiles {
		args = append(args, "--log-file="+file)
	}

	cmd := exec.Command(goAccessPath, args...)
	var stdout, stderr bytes.Buffer
	cmd.Stdout = &stdout
	cmd.Stderr = &stderr

	if err := cmd.Run(); err != nil {
		errMsg := strings.TrimSpace(stderr.String())
		if errMsg != "" {
			return nil, fmt.Errorf("goaccess: %s", errMsg)
		}
		return nil, fmt.Errorf("goaccess: %w", err)
	}

	return stdout.Bytes(), nil
}
